# We use comments to add note or comment to our program

# we can use comment to explain something about our code, we can use it as reminders
# to fix things or clear things up or we can use it to communicate things with other
# developers why we have written a code in a certain way. These are good use cases for using comment
# One thing we need to avoid when writing a comment is explaining what a code does.
# if certain assumptions has been made we can use comment to explain to remind yourself
# or other developers

# print sky is blue (bad comment) because its telling me the obvious
print("Sky is blue")

# calculate and return the square of a number(noisy, repetitive & distracting)
def square(number):
    return number * number

# So it's better to use comments to explain why's and how not what
